export default function translate(term, lang) {
  const word = dictionary[term];
  if (word === undefined) {
    console.error(`Can't find the word ${term} in dictionary`);
    return term;
  }
  const translation = word.find((e) => e.language === lang);
  if (translation === undefined) {
    console.error(`Can't find the translation for the word ${term} in ${lang}`);
    return term;
  }

  return translation.name;
}

export function translateGrowthRate(rate, lang) {
  const key = rate.replace(/-/g, "");
  const grate = growth_rate[key];
  if (grate === undefined)
    throw Error(`Can't find the rate ${rate} in dictionary`);
  const translation = grate.find((e) => e.language === lang);
  if (translation === undefined)
    throw Error(`Can't find the translation for the rate ${rate} in ${lang}`);
  return translation.name;
}

export function translateMoveLearnMethod(method, lang) {
  const key = removeDash(method);
  const m = move_learn_method[key];
  // console.log(m);
  if (m === undefined)
    throw Error(`Can't find the method ${method} in dictionary`);
  const translation = m.find((e) => e.language === lang);
  if (translation === undefined)
    throw Error(
      `Can't find the translation for the method ${method} in ${lang}`
    );
  return translation.name;
}

function removeDash(string) {
  return string.replace(/-/g, "");
}
